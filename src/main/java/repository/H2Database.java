    public SolicitudCrediticia insertSolicitud(String codigo, Cliente cliente, ProductoFinanciero producto, Usuario usuario,
                                               BigDecimal monto, int plazoMeses, String finalidad) {
        String sql = "INSERT INTO solicitudes(codigo, cliente_id, producto_id, usuario_id, monto_solicitado, plazo_meses, finalidad, estado, fecha_registro)\n" +
"VALUES(?,?,?,?,?,?,?,?,?)\n";
        try (Connection cn = getConnection(); PreparedStatement ps = cn.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS)) {
            ps.setString(1, codigo);
            ps.setInt(2, cliente.getId());
            ps.setInt(3, producto.getId());
            if (usuario == null) ps.setNull(4, Types.INTEGER); else ps.setInt(4, usuario.getId());
            ps.setBigDecimal(5, monto);
            ps.setInt(6, plazoMeses);
            ps.setString(7, finalidad);
            ps.setString(8, EstadoSolicitud.PENDIENTE.name());
            ps.setTimestamp(9, now());
            ps.executeUpdate();
            try (ResultSet keys = ps.getGeneratedKeys()) {
                keys.next();
                return new SolicitudCrediticia(keys.getInt(1), codigo, cliente, producto, usuario, monto, plazoMeses, finalidad);
            }
        } catch (SQLException e) {
            throw new IllegalStateException("No se pudo insertar solicitud", e);
        }
    }

