    public List<OperacionBancaria> listOperacionesByCliente(Cliente cliente) {
        if (cliente == null) return java.util.Collections.emptyList();
        return listOperacionesBySql("SELECT * FROM operaciones WHERE cliente_id=? ORDER BY id", cliente.getId());
    }

    private List<OperacionBancaria> listOperacionesBySql(String sql, Integer clienteId) {
        List<OperacionBancaria> list = new ArrayList<>();
        try (Connection cn = getConnection(); PreparedStatement ps = cn.prepareStatement(sql)) {
            if (clienteId != null) ps.setInt(1, clienteId);
            try (ResultSet rs = ps.executeQuery()) {
                while (rs.next()) list.add(mapOperacion(rs));
            }
            return list;
        } catch (SQLException e) {
            throw new IllegalStateException("No se pudieron listar operaciones", e);
        }
    }

    private Optional<OperacionBancaria> findOperacionById(Integer id) {
        if (id == null) return Optional.empty();
        try (Connection cn = getConnection(); PreparedStatement ps = cn.prepareStatement("SELECT * FROM operaciones WHERE id=?")) {
            ps.setInt(1, id);
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) return Optional.of(mapOperacion(rs));
            }
            return Optional.empty();
        } catch (SQLException e) {
            throw new IllegalStateException("No se pudo buscar operación", e);
        }
    }

    private OperacionBancaria mapOperacion(ResultSet rs) throws SQLException {
        Cliente cliente = findClienteById(rs.getInt("cliente_id")).orElseThrow(() -> new IllegalStateException("Registro relacionado no encontrado"));
        SolicitudCrediticia solicitud = findSolicitudById((Integer) rs.getObject("solicitud_id")).orElse(null);
        Usuario usuario = findUsuarioById((Integer) rs.getObject("usuario_id")).orElse(null);
        return new OperacionBancaria(rs.getInt("id"), cliente, solicitud, usuario, rs.getString("tipo_operacion"),
                rs.getBigDecimal("monto"), rs.getString("descripcion"));
    }

