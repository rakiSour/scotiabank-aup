    private List<SolicitudCrediticia> listSolicitudesBySql(String sql, Object param) {
        List<SolicitudCrediticia> list = new ArrayList<>();
        try (Connection cn = getConnection(); PreparedStatement ps = cn.prepareStatement(sql)) {
            if (param instanceof String) ps.setString(1, (String) param);
            if (param instanceof Integer) ps.setInt(1, (Integer) param);
            try (ResultSet rs = ps.executeQuery()) {
                while (rs.next()) list.add(mapSolicitud(rs));
            }
            return list;
        } catch (SQLException e) {
            throw new IllegalStateException("No se pudieron listar solicitudes", e);
        }
    }

    private SolicitudCrediticia mapSolicitud(ResultSet rs) throws SQLException {
        Cliente cliente = findClienteById(rs.getInt("cliente_id")).orElseThrow(() -> new IllegalStateException("Registro relacionado no encontrado"));
        ProductoFinanciero producto = findProductoById(rs.getInt("producto_id")).orElseThrow(() -> new IllegalStateException("Registro relacionado no encontrado"));
        Usuario usuario = findUsuarioById((Integer) rs.getObject("usuario_id")).orElse(null);
        SolicitudCrediticia solicitud = new SolicitudCrediticia(rs.getInt("id"), rs.getString("codigo"), cliente, producto, usuario,
                rs.getBigDecimal("monto_solicitado"), rs.getInt("plazo_meses"), rs.getString("finalidad"));
        solicitud.setEstado(EstadoSolicitud.valueOf(rs.getString("estado")));
        return solicitud;
    }

